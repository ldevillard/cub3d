#include "cub3d.h"

int		ft_check_second_arg(char *str)
{
	if (ft_strlen(str) == 6 && str[0] == '-'
			&& str[1] == '-' && str[2] == 's'
			&& str[3] == 'a' && str[4] == 'v'
			&& str[5] == 'e')
		return (1);
	else
		return (0);
}